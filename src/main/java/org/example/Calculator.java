package org.example;

public class Calculator {

    public int soma (int num1, int num2){
        return num1 + num2;
    }

    public int diff (int num1, int num2){
        return num1-num2;
    }

    public int multiply(int num1, int num2){
        return num1*num2;
    }

    public int divide(int num1, int num2){
        return num1/num2;
    }

}
